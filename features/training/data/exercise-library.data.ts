// ─── Quick lookup helpers ─────────────────────────────────────────

export function findExerciseById(id: string): Exercise | undefined {
  return EXERCISE_LIBRARY.find((ex) => ex.id === id)
}

export function searchExercises(query: string): Exercise[] {
  const q = query.toLowerCase().trim()
  if (!q) return EXERCISE_LIBRARY
  return EXERCISE_LIBRARY.filter(
    (ex) =>
      ex.name.toLowerCase().includes(q) ||
      ex.tags?.some((t) => t.toLowerCase().includes(q)) ||
      ex.aliases?.some((a) => a.toLowerCase().includes(q)) ||
      ex.muscleGroups.some((m) => m.toLowerCase().includes(q))
  )
}

export function filterExercises(
  exercises: Exercise[],
  filters: {
    muscle?: string
    equipment?: string
    category?: string
    difficulty?: string
    movementPattern?: string
  }
): Exercise[] {
  return exercises.filter((ex) => {
    if (filters.muscle && filters.muscle !== "all") {
      const allMuscles = [...ex.muscleGroups, ...(ex.secondaryMuscles ?? [])]
      if (!allMuscles.includes(filters.muscle as any)) return false
    }
    if (filters.equipment && filters.equipment !== "all") {
      if (!ex.equipment.includes(filters.equipment as any)) return false
    }
    if (filters.category && filters.category !== "all") {
      if (ex.category !== filters.category) return false
    }
    if (filters.difficulty && filters.difficulty !== "all") {
      if (ex.difficulty !== filters.difficulty) return false
    }
    if (filters.movementPattern && filters.movementPattern !== "all") {
      if (ex.movementPattern !== filters.movementPattern) return false
    }
    return true
  })
}
